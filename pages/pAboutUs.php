<h4> Phương thức thanh toán </h4>
<p>&emsp; Quý khách mua hàng tại MeShop có thể chọn các phương thức thanh toán sau đây: </p>

<p> 1. Thanh toán trực tiếp tại cửa hàng</p>

<p>&emsp; Thanh toán bằng tiền mặt hoặc bằng các loại thẻ thanh toán thẻ ghi nợ được chấp nhận theo quy định pháp luật hiện hành.</p>
<p> 2. Thanh toán qua ví điện tử</p>
<p>&emsp; Bước 1: Mở Ví điện tử, chọn “Quét Mã”</p>
<p>&emsp; Bước 2: Quét mã QR Di chuyển Camera để thấy và quét mã QR trê màn hình</p>
<p>&emsp; Bước 3: Nhập số tiền cần thanh toán</p>
<p>&emsp; Bước 4: Kiểm tra & Bấm “Xác nhận”

<h4> Cách thức yêu cầu trả hàng hoàn tiền (đối với khách hàng)  </h4>

<p>&emsp; Bước 1: Gửi yêu cầu trả hàng/hoàn tiền qua số điện thoại: 1900 1010</p>

<p>&emsp; Bước 2: Bộ phận CSKH sẽ liên hệ khách hàng xác minh thông tin và chuyển thông tin này về nội bộ xử lý</p>

<p>&emsp; Bước 3: Nhận thông báo chấp nhận trả hàng</p>

<p> Bộ phận CSKH của TGSF sẽ liên hệ khách hàng để thỏa thuận:</p>

<p>&emsp;- Đồng ý hoàn tiền: Quý khách hàng sẽ nhận được tiền hoàn trả theo chính sách hoàn tiền của TGSF</p>

<p>&emsp; - Không đồng ý hoàn tiền: Quý khách hàng sẽ nhận lại hàng đổi trả theo đúng cam kết chất lượng của TGSF.</p>

<p>&emsp; Bước 4: Đóng gói và trả hàng về cho TGSF thông qua đối tác vận chuyển của TGSF</p>

<p>&emsp; Bước 5: Hoàn tất thủ tục: Quý khách hàng sẽ nhận được tiền hoặc đơn hàng hoàn trả theo thỏa thuận với TGSF.</p>

